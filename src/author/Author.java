package author;

public class Author {

  private String name;
  private String surname;
  private Integer birthYear;
  private String city;
  private String country;

  public Author(String name, String surname, Integer birthYear, String city, String country) {

    this.name = name;
    this.surname = surname;
    this.birthYear = birthYear;
    this.city = city;
    this.country = country;
  }
}
